    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            Connection conn = Koneksi.getConnection();
            if (txtIdKelas.getText().isEmpty() || txtNamaKelas.getText().isEmpty() || txtKompetensi.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Make sure the data is filled in correctly!", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                PreparedStatement stmt = conn.prepareStatement("insert  into  kelas(id_kelas,  nama_kelas, kompetensi_keahlian) values(?,?,?)");
                stmt.setString(1, txtIdKelas.getText());
                stmt.setString(2, txtNamaKelas.getText());
                stmt.setString(3, txtKompetensi.getText());
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data saved successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
                TampilData();
                txtIdKelas.setText("");
                txtNamaKelas.setText("");
                txtKompetensi.setText("");
                btnSave.setEnabled(false);
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "Make sure the data is filled in correctly!", "Error", JOptionPane.ERROR_MESSAGE);

        }
        btnNew.setEnabled(true);
        btnEdit.setEnabled(false);
        btnDelete.setEnabled(false);
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            Connection conn = Koneksi.getConnection();
            if (txtIdKelas.getText().isEmpty() || txtNamaKelas.getText().isEmpty() || txtKompetensi.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Make sure the data is filled in correctly!", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                PreparedStatement stmt = conn.prepareStatement("update kelas set nama_kelas=?, kompetensi_keahlian=? where id_kelas=?");
                stmt.setString(1, txtNamaKelas.getText());
                stmt.setString(2, txtKompetensi.getText());
                stmt.setString(3, txtIdKelas.getText());
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Changed Successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
                TampilData();
                txtIdKelas.setText("");
                txtNamaKelas.setText("");
                txtKompetensi.setText("");
                btnEdit.setEnabled(false);
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        btnNew.setEnabled(true);
        btnSave.setEnabled(false);
        btnDelete.setEnabled(false);
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        Connection conn = Koneksi.getConnection();
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the data?", "Warning!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (confirm == 0) {
            try {
                java.sql.PreparedStatement stmt = conn.prepareStatement("delete from kelas where id_kelas ='" + txtIdKelas.getText() + "'");
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data deleted successfully", "Message", JOptionPane.INFORMATION_MESSAGE);
                TampilData();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data failed to delete" + e.getMessage(), "Message", JOptionPane.ERROR_MESSAGE);
            }
        }
        txtIdKelas.setText("");
        txtNamaKelas.setText("");
        txtKompetensi.setText("");
        btnNew.setEnabled(true);
        btnSave.setEnabled(false);
        btnEdit.setEnabled(false);
        btnDelete.setEnabled(false);
    }//GEN-LAST:event_btnDeleteActionPerformed

