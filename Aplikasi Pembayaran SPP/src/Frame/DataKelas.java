    public void TampilData() {
        DftTblModel_Spp = new DefaultTableModel();
        DftTblModel_Spp.addColumn("ID KELAS");
        DftTblModel_Spp.addColumn("NAMA KELAS");
        DftTblModel_Spp.addColumn("KOMPETENSI KEAHLIAN");
        tblKelas.setModel(DftTblModel_Spp);
        Connection conn = Koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from kelas";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_Spp.addRow(new Object[]{
                    res.getString("id_kelas"),
                    res.getString("nama_kelas"),
                    res.getString("kompetensi_keahlian")});
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
